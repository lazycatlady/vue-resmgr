-- ------------------------------------------------------

--  RESOURCE_TABLE_COLUMN 资源表列的表

-- ------------------------------------------------------

  CREATE TABLE `RESOURCE_TABLE_COLUMN` 
   (	`COLUMN_ID` INT(11) NOT NULL  COMMENT '列的id', 
	`TABLE_ID` VARCHAR(20) COMMENT '表的id', 
	`COLUMN_NAME` VARCHAR(255) COMMENT '列名', 
	`COLUMN_CNAME` VARCHAR(255) COMMENT '列中文名', 
	`ISQUERY` VARCHAR(255) COMMENT '字段是否可查询', 
	`QUERY_ORDER` INT(11) COMMENT '字段作为查询条件时的排序', 
	`ISUPDATE` VARCHAR(1) COMMENT '字段是否可更新', 
	`IS_EDIT` VARCHAR(1) COMMENT '字段修改时是否可编辑', 
	`UPDATE_ORDER` INT(11) COMMENT '更新页面中字段的排序', 
	`ISINSERT` VARCHAR(1) COMMENT '字段是否可插入', 
	`INSERT_ORDER` INT(11) COMMENT '新增页面中字段的排序', 
	`ISEXPORT` VARCHAR(1) COMMENT '是否可导出', 
	`EXPORT_ORDER` INT(11) COMMENT '数据导出时字段的排序', 
	`ISLIST` VARCHAR(1) COMMENT '字段是否列表展示', 
	`IS_FROZEN` VARCHAR(1) COMMENT '列表显示时该列是否冻结', 
	`IS_SORT` VARCHAR(1) COMMENT '列表显示时点击列标题是否可排序', 
	`LIST_ORDER` INT(11) COMMENT '表数据列表展示时的字段排序', 
	`LIST_STYLE` TEXT(2000) COMMENT '列表显示样式', 
	`ISINPORT` VARCHAR(1) COMMENT '字段是否可导入', 
	`PROPERTY_TYPE` VARCHAR(255) COMMENT '字段对应页面控件的类型', 
	`DEFAULT_VALUE` VARCHAR(255) COMMENT '新增时的默认值', 
	`TYPESQL` VARCHAR(2000) COMMENT '数据源sql语句（必须有ID和NAME字段）',
	`ALIAS_SQL` VARCHAR(2000) COMMENT '别名数据源sql语句（必须返回唯一的值）', 
	`DATA_TYPE` VARCHAR(1) COMMENT '数据类型（存储在数据库中的类型1字符串；2数字；3日期）', 
	`ATTRIBUTE_TYPE` VARCHAR(1) COMMENT '资源属性类型（1：数据库；2：自定义）', 
	`ISMUST` VARCHAR(1) COMMENT '是否是必填项', 
	`ISMULTI` VARCHAR(1) COMMENT '下拉列表是否多选', 
	`IS_SHOW_SEARCH` VARCHAR(1) COMMENT '下拉列表是否启用查询功能', 
	`IS_SHOW_FULLNAME` VARCHAR(1) COMMENT '树形字段是否显示全称', 
	`ISUNIQUE` VARCHAR(10) COMMENT '数据库中是否验证数据唯一性', 
	`VALIDDATATYPE` VARCHAR(10) COMMENT '验证类型', 
	`COLUMNLENGTH` VARCHAR(20) COMMENT '展示数据时该字段列表宽度', 
	`INPUTLENGTH` VARCHAR(20) COMMENT '输入内容最大长度', 
	`EACHCHECK` TEXT COMMENT '自定义验证方法', 
	`ENCRYPT` INT(11) COMMENT '是否加密（目前未使用）', 
	`ROWS_NUM` INT(4) COMMENT '文本域行数', 
	`CLOS_NUM` INT(4) COMMENT '文本域列数', 
	`PAGEURL` TEXT COMMENT '弹出页面的url', 
	`COLSPAN` VARCHAR(20) COMMENT '新增、修改、查看页面该列合并的单元格数', 
	`RELATION` VARCHAR(20) COMMENT '关联列', 
	`CASCADE` VARCHAR(255) COMMENT '级联更改列', 
	`STEP` VARCHAR(20) COMMENT '序号步长', 
	`ONCHANGE_METHOD` TEXT COMMENT 'onchange函数', 
	`ONCLICK_METHOD` TEXT COMMENT 'onclick函数', 
	`ONSELECT_METHOD` TEXT COMMENT 'onselect函数', 
	`ONFOCUS_METHOD` TEXT COMMENT 'onfocus函数', 
	`ONBLUR_METHOD` TEXT COMMENT 'onblur函数', 
	`ONKEYDOWN_METHOD` TEXT COMMENT 'onkeydown函数', 
	`ONKEYUP_METHOD` TEXT COMMENT 'onkeyup函数', 
	`ISOPENPAGE` VARCHAR(20) COMMENT '是否打开页面', 
	`DATETYPE` VARCHAR(255) COMMENT '日期时期格式', 
	`GROUPNAME` VARCHAR(100) COMMENT '该字段所属于的类别名称（用于属性分类显示）', 
	`REMARK` VARCHAR(200) COMMENT '备注', 
	`TOOLTIP`  VARCHAR(500) COMMENT '资源列输入提示',
	`ISFOREIGNKEY` VARCHAR(20) COMMENT '是否是外键', 
	`FOREIGNKEY_TABLENAME` VARCHAR(20) COMMENT '外键表名称', 
	`FOREIGNKEY_TABLE_COLUMN` VARCHAR(20) COMMENT '外键表列名',
	PRIMARY KEY (`COLUMN_ID`)
   ) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='资源表列的表' ;
